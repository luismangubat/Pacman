package pacman;

import java.awt.Color;

/**
 *
 * @author Luis
 */
public class Orbs {
     
    
    public Orbs(int x, int y, Color color, int width, int height) {
        this.x = x;
        this.y = y;
        
        this.color = color;
    }
    
    private int x;
    private int y;
    private Color color;

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }

    /**
     * @return the color
     */
    public Color getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(Color color) {
        this.color = color;
    }
   
}
